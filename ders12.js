// -----COMPARE OPERATORS-----


/*
  != : is equal to
  > : is greater than
  < : is smaller than
  >= : is greater than and equals to
  <= : is smaller than and equals to

*/

console.log(5 != 3);
let age1 = 20;
console.log(age1 != 20);


console.log(15 > 12);//true
console.log(2 > 5); //! false

console.log(12 < 15);//true
console.log(2 < 5);// also true
console.log(5 < 2);//! false

console.log(15 >= 15);//true
console.log(15 >= 18); //also true
console.log(15 >= 14);//! false
